public class t_cast
{
    public static void main(String[]args)
    {
        int il = 9;
        double dl = il;
        System.out.println(il);
        System.out.println(dl);

        // Manual Casting
        double d2 = 9.78d;
        int i2 = (int)d2;
        System.out.println(d2);
        System.out.println(i2);


    }
}
 
   